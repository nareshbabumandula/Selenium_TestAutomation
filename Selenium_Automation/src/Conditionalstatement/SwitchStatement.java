package Conditionalstatement;

public class SwitchStatement {

	public static void main(String[] args) {
		int i=-1;
		switch(i) {
		case 1:
			System.out.println("this is the first element");
			System.out.println("this is jaipal reddy");
			break;
		case 2:
			System.out.println("this is the second element");
		break;
		case 3:
			System.out.println("This is the third element");
			break;
		default:
			System.out.println("this is the Default number");
			break;
		
		
	}

	}

}
